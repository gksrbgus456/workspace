package ncs.test5;

public class Book {

	String title;
	String author;
	int price;
	String publishar;
	double discounRate;

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getAuthor() {
		return author;
	}

	public void setAuthor(String author) {
		this.author = author;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}

	public String getPublishar() {
		return publishar;
	}

	public void setPublishar(String publishar) {
		this.publishar = publishar;
	}

	public double getDiscounRate() {
		return discounRate;
	}

	public void setDiscounRate(double discounRate) {
		this.discounRate = discounRate;
	}

}
